export interface Population {
    total: number;
    female: GenderPopulationMetrics;
    male: GenderPopulationMetrics;
    percentage: number;
    year: number;
    full: string;
    source: string;
}

export interface LandArea {
    total: number;
    unit: string;
    full: string;
    source: string;
}
export interface District {
    code: DISTRICT_CODES;
    label: string;
    capital: string;
  //  population: Population; TODO: not enough data
   // landArea: LandArea;
  //  coordinates: Coordinates;
    administrativeLevel: ADMINISTRATIVE_LEVELS;
    regionCode: REGION_CODES;
    region?: Region;
}

export interface GenderPopulationMetrics {
    total: number;
    percentage: number;
}
export interface Region {
    code: REGION_CODES;
    label: string;
    capital: string;
    population: Population;
    districts?: District[];
    landArea: LandArea;
}

export interface RegionsQuery {
    /**
     * Include districts in response
     * @default false
     */
    includeDistricts?: boolean;
    /**
     * Include region name in search
     * @default true
     */
    region?: boolean;
    /**
     * Include regional capital in search
     * @default false
     */
    capital?: boolean;
    /**
     * Search query
     * @default ""
     */
    query?: string;
}

export interface DistrictsQuery {
    /**
     * Include regionInfo in response
     * @default false
     */
    includeRegion?: boolean;
    /**
     * Include district name in search
     * @default true
     */
    district?: boolean;
    /**
     * Include district capital in search
     * @default false
     */
    capital?: boolean;
    /**
     * Search query
     * @default ""
     */
    query?: string;
}


